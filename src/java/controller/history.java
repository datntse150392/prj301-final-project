    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User_Account user_account = (User_Account)session.getAttribute("user_account");
        Purchase_historyDAO purchase_historyDAO = new Purchase_historyDAO();
        ArrayList<Purchase_history> list = new ArrayList();
        list = purchase_historyDAO.getAllPurchase_historybyUserID(user_account.getUserid());
        request.setAttribute("list", list);
        request.getRequestDispatcher("history.jsp").forward(request, response);
    }

