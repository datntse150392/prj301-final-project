public class ListProduct {
    private int lid;
    private String lname;

    public ListProduct(int lid, String lname) {
        this.lid = lid;
        this.lname = lname;
    }

    public ListProduct() {
    }

    public int getLid() {
        return lid;
    }

    public void setLid(int lid) {
        this.lid = lid;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }
    
    
}
